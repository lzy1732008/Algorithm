class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None


class TreeLinkNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None


class RandomListNode:
    def __init__(self, x):
        self.label = x
        self.next = None
        self.random = None
